#Programa para determinar si un usuario es mayor de edad o no 
#solicitar al usuario que ingrese su edad

edad = int(input("Por favor, introduzca su edad: "))

#Utilizar los condicionales if-else para verificar si la edad incresada es mayor o igual a 18

if edad >= 18: print ("Eres mayor de edad")
else : print ("No eres mayor de edad")
